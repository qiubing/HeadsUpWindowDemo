package com.example.headsupwindowdemo;

import android.content.Context;
import android.view.LayoutInflater;
import android.view.MotionEvent;
import android.view.View;
import android.view.WindowManager;
import android.widget.LinearLayout;
import android.widget.TextView;

import java.lang.reflect.Field;

/**
 * Description:
 * Author: qiubing
 * Date: 2017-07-27 20:42
 */
public class FloatWindowSmallView extends LinearLayout {

    // 小悬浮窗的宽度
    public static int viewWith;

    // 小悬浮窗的高度
    public static int viewHeight;

    // 记录系统状态栏的高度
    private static int statusBarHeight;

    private WindowManager mWindowManager;

    // 小浮窗的参数
    private WindowManager.LayoutParams mParams;

    // 记录当前手指在屏幕上的x坐标值
    private float xInScreen;

    // 记录当前手指在屏幕上的y坐标值
    private float yInScreen;

    // 记录手指按下时在屏幕上的x坐标值
    private float xDownInScreen;

    // 记录手指按下时在屏幕上的y坐标值
    private float yDownInScreen;

    // 记录按下时在小浮窗的View上的x坐标值
    private float xInView;

    // 记录按下时在小浮窗的View上的y坐标值
    private float yInView;


    public FloatWindowSmallView(Context context) {
        super(context);
        mWindowManager = (WindowManager) context.getSystemService(Context.WINDOW_SERVICE);
        LayoutInflater.from(context).inflate(R.layout.float_window_small,this);
        View view  = findViewById(R.id.small_float_window_layout);
        viewHeight = view.getLayoutParams().height;
        viewWith = view.getLayoutParams().width;
        TextView percentView = (TextView)findViewById(R.id.percent);
        percentView.setText(MyWindowManager.getUsedPercentValue(context));
    }

    @Override
    public boolean onTouchEvent(MotionEvent event) {
        switch (event.getAction()){
            // 手指按下时记录必要数据,纵坐标的值都需要减去状态栏高度
            case MotionEvent.ACTION_DOWN:
                xInView = event.getX();
                yInView = event.getY();

                xDownInScreen = event.getRawX();
                yDownInScreen = event.getRawY() - getStatusBarHeight();

                xInScreen = event.getRawX();
                yInScreen = event.getRawY() - getStatusBarHeight();
                break;
            case MotionEvent.ACTION_MOVE:
                xInScreen = event.getRawX();
                yInScreen = event.getRawY();
                // 手指移动的时候更新小浮窗的位置
                updateViewPosition();

                break;
            case MotionEvent.ACTION_UP:
                // 如果手指离开屏幕时，xDownInScreen和xInScreen相等，且yDownInScreen和yInScreen相等，则视为触发了单击事件。
                if (xDownInScreen == xInScreen && yDownInScreen == yInScreen){
                    openBigWindow();
                }
                break;
            default:
                break;
        }
        return true;
    }

    //将小悬浮窗的参数传入，用于更新小悬浮窗的位置
    public void setParams(WindowManager.LayoutParams params){
        this.mParams = params;
    }

    //更新小悬浮窗在屏幕中的位置
    private void updateViewPosition(){
        mParams.x = (int) (xInScreen - xInView);
        mParams.y = (int) (yInScreen - yInView);
        mWindowManager.updateViewLayout(this,mParams);
    }

    private void openBigWindow(){
        MyWindowManager.createBigWindow(getContext());
        MyWindowManager.removeSmallWindow(getContext());
    }

    private int  getStatusBarHeight(){
        if (statusBarHeight == 0){
            try {
                Class<?> cls = Class.forName("com.android.internal.R$dime");
                Object object = cls.newInstance();
                Field field = cls.getField("status_bar_height");
                int x = (Integer) field.get(object);
                statusBarHeight = getResources().getDimensionPixelSize(x);
            } catch (ClassNotFoundException e) {
                e.printStackTrace();
            } catch (InstantiationException e) {
                e.printStackTrace();
            } catch (IllegalAccessException e) {
                e.printStackTrace();
            } catch (NoSuchFieldException e) {
                e.printStackTrace();
            }
        }
        return statusBarHeight;
    }
}
